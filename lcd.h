//LCD写入指令
void LCD_Write_Cmd(int data_tmp)
{
    char i;

    digitalWrite(LCD_CS, LOW);   //选中LCD
    digitalWrite(LCD_RS, LOW);   //选择数据寄存器

    for (i = 0; i < 8; i++)
    {
        digitalWrite(LCD_SCK, LOW);   //选中LCD
        if (data_tmp & 0x80)
        {
            digitalWrite(LCD_MOSI, HIGH);
        }
        else
        {
            digitalWrite(LCD_MOSI, LOW);
        }

        digitalWrite(LCD_SCK, HIGH);

        data_tmp = data_tmp <<= 1;
    }
}
//LCD写入数据
void LCD_Write_Data(int data_temp)
{
    char i;
    digitalWrite(LCD_CS, LOW);   //选中LCD
    digitalWrite(LCD_RS, HIGH);  //选择数据寄存器

    for (i = 0; i < 8; i++)
    {
        digitalWrite(LCD_SCK, LOW);


        if (data_temp & 0x80){  //判断当前最左位是1还是0
            digitalWrite(LCD_MOSI, HIGH);
        }
        else{
            digitalWrite(LCD_MOSI, LOW);
        }

        digitalWrite(LCD_SCK, HIGH);

        data_temp = data_temp <<= 1; //数据左移写入 

    }
}
//初始化LCD程序
void LCD_Init()
{

    LCD_Reset();

    LCD_Write_Cmd(0xe2);
    LCD_Write_Cmd(0x2e);
    LCD_Write_Cmd(0x2f);
    LCD_Write_Cmd(0x23);
    LCD_Write_Cmd(0x81);
    LCD_Write_Cmd(0x28);
    LCD_Write_Cmd(0xa2);
    LCD_Write_Cmd(0xc8);
    LCD_Write_Cmd(0xa0);
    LCD_Write_Cmd(0xaf);
}
void LCD_Write_Address(unsigned char Row, unsigned char Column)
{
    //写入地址
    LCD_Write_Cmd(0xb0 + Row); //0xB0-0xB7 64个像素被分成8行 
    LCD_Write_Cmd(0x10 + (Column >> 4));        //列的高4位地址
    LCD_Write_Cmd(Column & 0xF);  //列的低4位地址
}
