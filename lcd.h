void LCD_Reset()
{
    digitalWrite(LCD_RST, LOW);    // turn the LED on (HIGH is the voltage level)  
    delay(10);
    digitalWrite(LCD_RST, HIGH);   // turn the LED on (HIGH is the voltage level)     
    delay(10);
}
void LCD_Fill(unsigned char Row, unsigned char Column, unsigned char length, unsigned char Fill_data)
{
    LCD_Write_Address(Row, Column);
    for (int i = 0; i < length; i++){
        LCD_Write_Data(Fill_data);
    }
}
void LCD_Pins_Init()
{
    pinMode(LCD_CS, OUTPUT);
    pinMode(LCD_RST, OUTPUT);
    pinMode(LCD_RS, OUTPUT);
    pinMode(LCD_SCK, OUTPUT);
    pinMode(LCD_MOSI, OUTPUT);
}
